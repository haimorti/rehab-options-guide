def E(text):
    return html.escape(text, quote=False)

def p(text, cls="par"):
    return f'<p class="{cls}">{E(text)}</p>'

def bullet_list(items):
    return '<ul class="checks">' + ''.join(f'<li>{svg("check",16)}<span>{E(item)}</span></li>' for item in items) + '</ul>'


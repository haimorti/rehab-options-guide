def breadcrumb(pid):
    crumbs = []
    cur = pid
    while cur:
        crumbs.append(PAGES[cur]["title"])
        cur = PAGES[cur].get("parent")
    return list(reversed(crumbs))

def nav_cards(ids):
    cards = []
    for cid in ids:
        page = PAGES[cid]
        cards.append(f'''<a class="nav-card" href="#{cid}">
  <span class="nav-ico">{svg(page["icon"],22)}</span>
  <span class="nav-text"><strong>{E(page["title"])}</strong><small>{E(CARD_DESCRIPTIONS[cid])}</small></span>
  <span class="nav-arrow">{svg("arrow",18)}</span>
</a>''')
    return '<div class="nav-grid">' + ''.join(cards) + '</div>'

def page_actions(pid):
    if pid == "home":
        return ""
    parent = PAGES[pid].get("parent", "home")
    links = [f'<a class="back-link" href="#home">{svg("home",15)} חזרה לעמוד הבית</a>']
    if parent and parent != "home":
        links.append(f'<a class="back-link" href="#{parent}">{svg("arrow",15)} חזרה אל {E(PAGES[parent]["title"])}</a>')
    return '<div class="page-actions">' + ''.join(links) + '</div>'

def inner_header(pid):
    if pid == "home":
        return ""
    return f'<div class="breadcrumb-line">{" <span>←</span> ".join(E(c) for c in breadcrumb(pid))}</div>{page_actions(pid)}'

